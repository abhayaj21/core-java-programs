class AllCalulation
{
	public static void main(String args[])
	{
		int no1,no2,add,sub,mul,div;
		no1 = 50;
		no2 = 10;
		add = no1 + no2;
		sub = no1 - no2;
		mul = no1 * no2;
		div = no1 / no2;

		System.out.println("Number 1 = " + no1);
		System.out.println("Number 2 = " + no2);
		System.out.println("Addition = " + add);
		System.out.println("Substracion = " + sub);
		System.out.println("Multiplicaiton = " + mul);
		System.out.println("Division = " + div);
	}
}