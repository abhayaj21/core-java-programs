class Rectangle
{
	public static void main(String args[])
	{
		int length,width,area,para;
		length = 10;
		width = 20;
		area = length * width;
		para = 2 * (length + width);
		System.out.println("Length = " + length);
		System.out.println("Width = " + width);
		System.out.println("Area = " + area);
		System.out.println("Parameter = " + para);
	}
}